    public GUIAccessoUtente() {

        this.controllerAccessoUtente = new ControllerAccessoUtente();

        caricaLogo();

        annullaButton.addActionListener(new ActionListener() {

            /**
             * Gestisce l'annullamento dell'accesso e riporta alla Home Page.

             * @param e evento generato dalla pressione del pulsante Annulla
             */

            @Override
            public void actionPerformed(ActionEvent e) {
                tornaAllaHomePage();
            }
        });

        accediButton.addActionListener(new ActionListener() {

            /**
             * Gestisce la richiesta di accesso dell'utente.
             * Acquisisce i dati inseriti nella GUI e li inoltra al metodo
             * responsabile della delega verso il ControllerAccessoUtente.

             * @param e evento generato dalla pressione del pulsante Accedi
             */

            @Override
            public void actionPerformed(ActionEvent e) {

                Map<String, Object> datiAccesso = acquisisciDatiAccesso();
                inviaDatiAccesso(datiAccesso);
            }
        });
    }

    /**
     * Acquisisce i dati inseriti dall'utente nella GUI di accesso.

     * La Boundary raccoglie esclusivamente dati semplici, cioè email e password,
     * e li organizza in una Map<String, Object> da inoltrare al livello Control.
     * Il metodo non crea Credenziali, non accede a Utente e non effettua
     * verifiche sulla persistenza, rispettando la separazione BCED tra
     * Boundary, Control, Entity e Database.
     * @return mappa contenente email e password inserite dall'utente
     */

    public Map<String, Object> acquisisciDatiAccesso() {

        Map<String, Object> datiAccesso = new HashMap<>();

        datiAccesso.put("email", valoreTestuale(txtEmail.getText()));

        datiAccesso.put("password", new String(txtPassword.getPassword()));

        return datiAccesso;
    }

    /**
     * Invia i dati di accesso al ControllerAccessoUtente.
     *
     * La Boundary acquisisce le credenziali inserite dall'utente,
     * verifica formalmente la presenza dei campi obbligatori e delega
     * al ControllerAccessoUtente la verifica della precondizione di accesso.
     *
     * L'accesso non è gestito dal ControllerServiziSistema dei casi d'uso
     * principali, ma dal controller dedicato alla precondizione di autenticazione.
     *
     * @param datiAccesso dati semplici contenenti email e password
     */
    public void inviaDatiAccesso(Map<String, Object> datiAccesso) {

        if (!validaCampiAccesso(datiAccesso)) {
            segnalaErroreAccesso(
                    "Inserisci email e password per effettuare l'accesso."
            );
            return;
        }

        Map<String, Object> datiSessioneUtente =
                controllerAccessoUtente.verificaCredenzialiAccesso(datiAccesso);

        if (datiSessioneUtente == null || datiSessioneUtente.isEmpty()) {
            segnalaErroreAccesso(
                    "Credenziali non valide. Verifica email e password."
            );
            return;
        }

        comunicaAccessoRiuscito();
        apriDashboard(datiSessioneUtente);
    }


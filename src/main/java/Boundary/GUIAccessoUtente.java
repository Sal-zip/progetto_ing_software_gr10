    public void comunicaAccessoRiuscito() {
        JOptionPane.showMessageDialog(
                contentPanel,
                "Accesso effettuato con successo.",
                "Accesso riuscito",
                JOptionPane.INFORMATION_MESSAGE
        );
    }

    /**
     * Comunica all'utente un errore relativo alla procedura di accesso.
     *
     * La Boundary gestisce esclusivamente la comunicazione grafica dell'esito
     * negativo, mentre il livello Control restituisce solo un esito semplice
     * relativo alla validità delle credenziali.
     *
     * @param messaggio descrizione dell'errore da mostrare all'utente
     */
    public void segnalaErroreAccesso(String messaggio) {

        String messaggioDaMostrare = valoreTestuale(messaggio);

        if (isVuoto(messaggioDaMostrare)) {
            messaggioDaMostrare = "Accesso non completato. Verifica le credenziali inserite.";
        }

        JOptionPane.showMessageDialog(
                contentPanel,
                messaggioDaMostrare,
                "Errore accesso",
                JOptionPane.ERROR_MESSAGE
        );
    }

    /**
     * Apre la dashboard dell'utente autenticato e chiude la finestra di accesso.
     *
     * Questo metodo è un supporto tecnico della Boundary per la navigazione
     * tra schermate. Non rappresenta una responsabilità di dominio e non viene
     * inserito nel modello Entity.
     *
     * Il metodo riceve solo dati semplici di sessione prodotti dal livello
     * Control/Facade Entity, senza ricevere né propagare istanze Entity di Utente.
     *
     * @param datiSessioneUtente mappa contenente idUtente, nome, cognome e ruolo
     */
    private void apriDashboard(Map<String, Object> datiSessioneUtente) {

        if (!validaDatiSessioneUtente(datiSessioneUtente)) {
            segnalaErroreAccesso(
                    "Sessione utente non valida. Effettua nuovamente l'accesso."
            );
            return;
        }

        GUIDashboardUtente guiDashboard = new GUIDashboardUtente(datiSessioneUtente);

        JFrame frameDashboard = new JFrame("Dashboard Utente");
        frameDashboard.setContentPane(guiDashboard.getContentPanel());
        frameDashboard.setDefaultCloseOperation(JFrame.DISPOSE_ON_CLOSE);
        frameDashboard.setResizable(false);
        frameDashboard.pack();
        frameDashboard.setLocationRelativeTo(null);
        frameDashboard.setVisible(true);

        Window finestraCorrente = SwingUtilities.getWindowAncestor(contentPanel);

        if (finestraCorrente != null) {
            finestraCorrente.dispose();
        }
    }

    /**
     * Verifica che i campi necessari per l'accesso siano stati compilati.
     * La validazione è solo formale e riguarda i dati acquisiti dalla Boundary:
     * controlla che email e password non siano vuote.
     * La verifica effettiva delle credenziali non viene effettuata nella GUI,
     * ma resta responsabilità del livello Control e del Facade Entity.
     * @param datiAccesso mappa contenente i dati acquisiti dalla GUI
     * @return true se email e password sono valorizzate, false altrimenti
     */

    private boolean validaCampiAccesso(Map<String, Object> datiAccesso) {

        if (datiAccesso == null) {
            return false;
        }
        String email = valoreTestuale(datiAccesso.get("email"));

        String password = valoreTestuale(datiAccesso.get("password"));

        return !isVuoto(email) && !isVuoto(password);
    }

    /**
     * Converte un valore generico in una stringa pulita.
     * Metodo privato di supporto usato dalla Boundary per normalizzare
     * i dati acquisiti dai componenti Swing prima di inoltrarli al livello Control.
     * @param valore valore da convertire
     * @return stringa senza spazi iniziali/finali, oppure stringa vuota se il valore è nullo
     */

    private String valoreTestuale(Object valore) {

        if (valore == null) {

            return "";
        }
        return valore.toString().trim();
    }

    /**
     * Verifica se una stringa è nulla o vuota.
     * Metodo privato di supporto alla validazione formale dei campi della GUI.
     * Non espone logica di dominio e non accede alle Entity.
     * @param valore stringa da verificare
     * @return true se il valore è nullo o vuoto, false altrimenti
     */

    private boolean isVuoto(String valore) {

        return valore == null || valore.trim().isEmpty();
    }

    /**
     * Verifica che i dati semplici di sessione siano utilizzabili dalla dashboard.

     * La Boundary non verifica credenziali e non accede al dominio: controlla
     * soltanto che la Map ricevuta dal controller contenga almeno l'identificativo
     * dell'utente autenticato e il ruolo necessario alla configurazione della
     * dashboard.

     * @param datiSessioneUtente mappa contenente i dati semplici dell'utente autenticato
     * @return true se i dati minimi di sessione sono presenti, false altrimenti

     */

    private boolean validaDatiSessioneUtente(Map<String, Object> datiSessioneUtente) {

        if (datiSessioneUtente == null) {

            return false;

        }

        Object idUtente = datiSessioneUtente.get("idUtente");

        String ruolo = valoreTestuale(datiSessioneUtente.get("ruolo"));

        return idUtente != null && !isVuoto(ruolo);
    }
